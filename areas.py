# エリア名 → エリアコードの逆引きマップ
AREA_NAME_MAP: dict[str, str] = {v: k for k, v in AREA_CODE_MAP.items()}


def search_area_by_name(name: str) -> list[dict[str, str]]:
    """
    エリア名の部分一致でエリアコードを検索する

    Args:
        name: 検索キーワード（例: "沖縄", "東京"）

    Returns:
        マッチしたエリアのリスト [{"code": "...", "name": "..."}]
    """
    results = []
    for code, area_name in AREA_CODE_MAP.items():
        if name in area_name:
            results.append({"code": code, "name": area_name})
    return results
